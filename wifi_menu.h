// -------- Button‑controlled on‑screen keyboard ----------
String getCustomSSID() {
  const char* keyboardRows[] = {
    "ABCDEFGHIJKLM",
    "NOPQRSTUVWXYZ",
    "abcdefghijklm",
    "nopqrstuvwxyz",
    "0123456789_- ",
    "BACKSPACE",
    "DONE"
  };
  int rowCount = 7;
  int keyW = 16, keyH = 20, startX = 10, startY = 70, spacing = 2;

  String input = "";
  int selectedRow = 0;
  int selectedCol = 0;

  auto draw = [&]() {
    tft.fillRect(0, 23, 240, 297, BG);
    drawStatusBarBright();
    tft.setTextColor(WHITE, BG);
    tft.setCursor(10, 40);
    tft.print("Enter Custom SSID:");
    tft.fillRect(10, 55, 220, 25, TILE);
    tft.drawRect(10, 55, 220, 25, BORDER);
    tft.setTextColor(WHITE, TILE);
    tft.setCursor(15, 62);
    tft.print(input);
    for (int r = 0; r < rowCount; r++) {
      int y = startY + r * (keyH + spacing);
      int x = startX;
      const char* keys = keyboardRows[r];
      int len = strlen(keys);
      for (int c = 0; c < len; c++) {
        uint16_t bg = (r == selectedRow && c == selectedCol) ? SELECT : TILE;
        uint16_t txt = (r == selectedRow && c == selectedCol) ? BG : WHITE;
        tft.fillRect(x, y, keyW, keyH, bg);
        tft.drawRect(x, y, keyW, keyH, BORDER);
        tft.setTextColor(txt, bg);
        tft.setCursor(x + 3, y + 4);
        tft.print(keys[c]);
        x += keyW + spacing;
      }
    }
  };

  draw();

  while (true) {
    if (readButtonOnce(BTN_UP)) {
      selectedRow = (selectedRow - 1 + rowCount) % rowCount;
      selectedCol = 0;
      draw();
      delay(150);
    }
    if (readButtonOnce(BTN_DOWN)) {
      selectedRow = (selectedRow + 1) % rowCount;
      selectedCol = 0;
      draw();
      delay(150);
    }
    if (readButtonOnce(BTN_LEFT)) {
      int len = strlen(keyboardRows[selectedRow]);
      if (len > 0) selectedCol = (selectedCol - 1 + len) % len;
      draw();
      delay(150);
    }
    if (readButtonOnce(BTN_RIGHT)) {
      int len = strlen(keyboardRows[selectedRow]);
      if (len > 0) selectedCol = (selectedCol + 1) % len;
      draw();
      delay(150);
    }
    if (readButtonOnce(BTN_OK)) {
      const char* keys = keyboardRows[selectedRow];
      if (selectedRow == 5) { // BACKSPACE
        if (input.length() > 0) input.remove(input.length() - 1);
      } else if (selectedRow == 6) { // DONE
        if (input.length() > 0) return input;
        else return "Free_WiFi"; // fallback if empty
      } else {
        char c = keys[selectedCol];
        if (input.length() < 25) input += c;
      }
      draw();
      delay(150);
    }
  }
}

