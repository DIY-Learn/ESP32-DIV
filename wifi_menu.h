void initSDCard() {
  if (sdCardReady) return;
  SPI.begin(SD_SCK_PIN, SD_MISO_PIN, SD_MOSI_PIN, SD_CS_PIN);
  if (!SD.begin(SD_CS_PIN, SPI)) {
    Serial.println("SD Card init failed");
    sdCardReady = false;
  } else {
    sdCardReady = true;
    Serial.println("SD Card ready");
  }
}

void writePcapGlobalHeader() {
  uint32_t magic = 0xa1b2c3d4;
  uint16_t version_major = 2;
  uint16_t version_minor = 4;
  uint32_t thiszone = 0;
  uint32_t sigfigs = 0;
  uint32_t snaplen = 65535;
  uint32_t linktype = 127;   // 802.11 (raw, no radiotap)
  pcapFile.write((uint8_t*)&magic, 4);
  pcapFile.write((uint8_t*)&version_major, 2);
  pcapFile.write((uint8_t*)&version_minor, 2);
  pcapFile.write((uint8_t*)&thiszone, 4);
  pcapFile.write((uint8_t*)&sigfigs, 4);
  pcapFile.write((uint8_t*)&snaplen, 4);
  pcapFile.write((uint8_t*)&linktype, 4);
}

// Promiscuous callback that writes packets to PCAP file
void promiscuous_pcap_cb(void *buf, wifi_promiscuous_pkt_type_t type) {
  if (!monitorRunning) return;
  packetCount++;
  if (!pcapWriting) return;

  wifi_promiscuous_pkt_t *pkt = (wifi_promiscuous_pkt_t*)buf;
  uint64_t ts = pkt->rx_ctrl.timestamp;
  uint32_t ts_sec = ts / 1000000;
  uint32_t ts_usec = ts % 1000000;
  uint32_t incl_len = pkt->rx_ctrl.sig_len;
  uint32_t orig_len = incl_len;
  if (incl_len > 65535) incl_len = 65535;

  pcapFile.write((uint8_t*)&ts_sec, 4);
  pcapFile.write((uint8_t*)&ts_usec, 4);
  pcapFile.write((uint8_t*)&incl_len, 4);
  pcapFile.write((uint8_t*)&orig_len, 4);
  pcapFile.write(pkt->payload, incl_len);
  pcapPacketCount++;
}

