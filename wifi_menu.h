// ========== CALLBACK FUNCTIONS (global scope, outside any function) ==========
void promiscuous_detector_cb(void *buf, wifi_promiscuous_pkt_type_t type) {
  wifi_promiscuous_pkt_t *pkt = (wifi_promiscuous_pkt_t*)buf;
  if ((pkt->payload[0] & 0xFC) == 0xC0) {
    deauthDetected = true;
    uint8_t* src = &pkt->payload[10];
    uint8_t* dst = &pkt->payload[4];
    char temp[64];
    sprintf(temp, "Deauth %02X:%02X:%02X:%02X:%02X:%02X -> %02X:%02X:%02X:%02X:%02X:%02X",
            src[0],src[1],src[2],src[3],src[4],src[5],
            dst[0],dst[1],dst[2],dst[3],dst[4],dst[5]);
    detectedDeauthInfo = String(temp);
  }
}

void promiscuous_station_cb(void *buf, wifi_promiscuous_pkt_type_t type) {
  wifi_promiscuous_pkt_t *pkt = (wifi_promiscuous_pkt_t*)buf;
  uint8_t frameType = pkt->payload[0] & 0xFC;
  if (frameType == 0x40 || frameType == 0x80 || frameType == 0x00 || frameType == 0x20) {
    uint8_t* addr2 = &pkt->payload[10];
    char mac[18];
    sprintf(mac, "%02X:%02X:%02X:%02X:%02X:%02X", addr2[0],addr2[1],addr2[2],addr2[3],addr2[4],addr2[5]);
    String macStr = String(mac);
    bool found = false;
    for (size_t i = 0; i < stationMACs.size(); i++) {
      if (stationMACs[i] == macStr) { found = true; break; }
    }
    if (!found && macStr != "FF:FF:FF:FF:FF:FF" && macStr != "00:00:00:00:00:00") {
      stationMACs.push_back(macStr);
      stationIPs.push_back("unknown");
      stationHostnames.push_back("unknown");
    }
  }
}
