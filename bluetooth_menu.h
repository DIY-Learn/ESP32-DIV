// Helper: list scripts on SD
void listScripts() {
  tft.fillRect(0, 23, 240, 297, BG);
  drawStatusBarBright();
  tft.setTextColor(WHITE, BG);
  tft.setCursor(10, 35);
  tft.print("Saved Scripts");

  initSD();
  if (!SD.begin(SD_CS_PIN, sdSPI)) {
    tft.setCursor(10, 55);
    tft.setTextColor(RED, BG);
    tft.print("SD Card not found!");
    delay(2000);
    return;
  }
  if (!SD.exists("/ducky")) SD.mkdir("/ducky");
  File dir = SD.open("/ducky");
  std::vector<String> files;
  while (true) {
    File entry = dir.openNextFile();
    if (!entry) break;
    if (!entry.isDirectory()) {
      String name = entry.name();
      if (name.endsWith(".txt")) files.push_back(name);
    }
    entry.close();
  }
  dir.close();

  if (files.size() == 0) {
    tft.setCursor(10, 55);
    tft.setTextColor(YELLOW, BG);
    tft.print("No scripts found.");
    tft.setCursor(10, 75);
    tft.print("Create one with keyboard.");
    delay(2000);
    return;
  }

  int idx = 0;
  while (true) {
    tft.fillRect(10, 55, 220, 200, BG);
    tft.setTextColor(WHITE, BG);
    for (size_t i = 0; i < files.size() && i < 10; i++) {
      int y = 65 + i * 20;
      tft.setTextColor((i == idx) ? SELECT : GREEN, BG);
      tft.setCursor(20, y);
      tft.print(files[i]);
    }
    if (readButtonOnce(BTN_UP)) { idx = (idx - 1 + files.size()) % files.size(); delay(150); }
    if (readButtonOnce(BTN_DOWN)) { idx = (idx + 1) % files.size(); delay(150); }
    if (readButtonOnce(BTN_OK)) {
      String selectedScript = files[idx];
      tft.fillRect(0, 23, 240, 297, BG);
      drawStatusBarBright();
      tft.setTextColor(WHITE, BG);
      tft.setCursor(10, 35);
      tft.print("Script: " + selectedScript);
      File f = SD.open("/ducky/" + selectedScript);
      String content = f.readString();
      f.close();
      tft.setCursor(10, 55);
      tft.print("Size: " + String(content.length()) + " bytes");
      tft.setCursor(10, 75);
      tft.print("Press OK to execute");
      tft.setCursor(10, 95);
      tft.print("Press RIGHT to delete");
      tft.setCursor(10, 115);
      tft.print("Press LEFT to return");

      while (true) {
        if (readButtonOnce(BTN_OK)) {
          BleKeyboard bleKeyboard;
          bleKeyboard.begin();
          tft.fillRect(10, 135, 220, 20, BG);
          tft.setTextColor(YELLOW, BG);
          tft.setCursor(10, 135);
          tft.print("Waiting for BLE connection...");
          while (!bleKeyboard.isConnected()) delay(100);
          bleKeyboard.print(content);
          bleKeyboard.end();
          tft.fillRect(10, 135, 220, 20, BG);
          tft.setTextColor(GREEN, BG);
          tft.setCursor(10, 135);
          tft.print("Executed!");
          delay(1500);
          break;
        }
        if (readButtonOnce(BTN_RIGHT)) {
          SD.remove("/ducky/" + selectedScript);
          tft.setCursor(10, 135);
          tft.setTextColor(RED, BG);
          tft.print("Deleted!");
          delay(1000);
          break;
        }
        if (readButtonOnce(BTN_LEFT)) break;
        delay(50);
      }
      break;
    }
    if (readButtonOnce(BTN_LEFT)) break;
    delay(50);
  }
}

// ----- On‑screen keyboard -----
String onScreenKeyboard(String prompt) {
  const char* rows[] = {
    "ABCDEFGHIJKLM",
    "NOPQRSTUVWXYZ",
    "abcdefghijklm",
    "nopqrstuvwxyz",
    "0123456789",
    "!@#$%^&*()",
    "-_=+[]{}|;:'",
    ",.<>/?`~",
    "BACKSPACE",
    "DONE"
  };
  int rowCount = 10;
  int keyW = 15;
  int keyH = 18;
  int startX = 5;
  int startY = 80;
  int spacing = 1;
  String input = "";
  int selRow = 0, selCol = 0;

  auto draw = [&]() {
    tft.fillRect(0, 23, 240, 297, BG);
    drawStatusBarBright();
    tft.setTextColor(WHITE, BG);
    tft.setCursor(10, 40);
    tft.print(prompt);
    tft.fillRect(10, 55, 220, 25, TILE);
    tft.drawRect(10, 55, 220, 25, BORDER);
    tft.setTextColor(WHITE, TILE);
    tft.setCursor(15, 62);
    tft.print(input);
    for (int r = 0; r < rowCount; r++) {
      int y = startY + r * (keyH + spacing);
      int x = startX;
      const char* keys = rows[r];
      int len = strlen(keys);
      for (int c = 0; c < len; c++) {
        uint16_t bg = (r == selRow && c == selCol) ? SELECT : TILE;
        uint16_t txt = (r == selRow && c == selCol) ? BG : WHITE;
        tft.fillRect(x, y, keyW, keyH, bg);
        tft.drawRect(x, y, keyW, keyH, BORDER);
        tft.setTextColor(txt, bg);
        tft.setCursor(x + 3, y + 4);
        tft.print(keys[c]);
        x += keyW + spacing;
      }
    }
    tft.fillRect(10, 260, 100, 30, TILE);
    tft.drawRect(10, 260, 100, 30, BORDER);
    tft.setTextColor(WHITE, TILE);
    tft.setCursor(40, 270);
    tft.print("SAVE");
    tft.fillRect(130, 260, 100, 30, TILE);
    tft.drawRect(130, 260, 100, 30, BORDER);
    tft.setCursor(155, 270);
    tft.print("CANCEL");
  };

  draw();
  while (true) {
    if (readButtonOnce(BTN_UP)) {
      selRow = (selRow - 1 + rowCount) % rowCount;
      selCol = 0;
      draw();
      delay(150);
    }
    if (readButtonOnce(BTN_DOWN)) {
      selRow = (selRow + 1) % rowCount;
      selCol = 0;
      draw();
      delay(150);
    }
    if (readButtonOnce(BTN_LEFT)) {
      int len = strlen(rows[selRow]);
      if (len > 0) selCol = (selCol - 1 + len) % len;
      draw();
      delay(150);
    }
    if (readButtonOnce(BTN_RIGHT)) {
      int len = strlen(rows[selRow]);
      if (len > 0) selCol = (selCol + 1) % len;
      draw();
      delay(150);
    }
    if (readButtonOnce(BTN_OK)) {
      const char* keys = rows[selRow];
      if (selRow == rowCount - 2) {
        if (input.length() > 0) input.remove(input.length() - 1);
      } else if (selRow == rowCount - 1) {
        if (input.length() == 0) {
          tft.fillRect(10, 300, 220, 20, BG);
          tft.setTextColor(RED, BG);
          tft.setCursor(10, 300);
          tft.print("Cannot save empty script!");
          delay(1000);
          draw();
          continue;
        }
        return input;
      } else {
        char c = keys[selCol];
        if (input.length() < 200) input += c;
      }
      draw();
      delay(150);
    }
    delay(50);
  }
}

