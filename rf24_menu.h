// ========== Menu handler ==========
bool handleRF24Menu() {
  int old = rf24Selected;
  bool redraw = false;

  if (readButtonOnce(BTN_UP)) {
    rf24Selected = (rf24Selected - 1 + RF24_ATTACK_COUNT) % RF24_ATTACK_COUNT;
    redraw = true;
  }
  else if (readButtonOnce(BTN_DOWN)) {
    rf24Selected = (rf24Selected + 1) % RF24_ATTACK_COUNT;
    redraw = true;
  }
  else if (readButtonOnce(BTN_OK)) {
    if (rf24Selected == RF24_ATTACK_COUNT - 1) return true;
    switch (rf24Selected) {
      case 0: runNRF24Jammer(); break;
      case 1: runNRF24Scanner(); break;
    }
    drawRF24Menu();
    redraw = false;
  }

  if (redraw) {
    int oldY = 55 + old * 36 + (old == RF24_ATTACK_COUNT - 1 ? 5 : 0);
    int newY = 55 + rf24Selected * 36 + (rf24Selected == RF24_ATTACK_COUNT - 1 ? 5 : 0);
    tft.fillRect(50, oldY - 8, 150, 20, BG);
    tft.setTextColor(GREEN, BG);
    tft.setCursor(50, oldY);
    tft.print(rf24Attacks[old]);
    tft.fillRect(50, newY - 8, 150, 20, BG);
    tft.setTextColor(SELECT, BG);
    tft.setCursor(50, newY);
    tft.print(rf24Attacks[rf24Selected]);
  }
  return false;
}

// ========== Attack: NRF24 Jammer ==========
void runNRF24Jammer() {
  tft.fillRect(0, 23, 240, 297, BG);
  drawStatusBarBright();
  tft.setTextColor(GREEN, BG);
  tft.setCursor(10, 35);
  tft.print("NRF24 Jammer");
  tft.setCursor(10, 55);
  tft.print("OK: start/stop");
  tft.setCursor(10, 75);
  tft.print("LEFT: exit");

  SPI.begin(NRF_SCK_PIN, NRF_MISO_PIN, NRF_MOSI_PIN);
  RF24 radio(NRF_CE_PIN, NRF_CSN_PIN);
  
  if (!radio.begin()) {
    tft.setTextColor(RED, BG);
    tft.setCursor(10, 95);
    tft.print("NRF24L01 NOT DETECTED!");
    tft.setCursor(10, 115);
    tft.print("Check wiring.");
    delay(2000);
    return;
  }
  
  radio.setAutoAck(false);
  radio.stopListening();
  radio.setRetries(0, 0);
  radio.setPALevel(RF24_PA_MAX, true);
  radio.setDataRate(RF24_2MBPS);
  radio.setCRCLength(RF24_CRC_DISABLED);

  bool active = false;
  const int jamChannels[] = {2, 26, 80, 37, 38, 39};
  int jamCount = sizeof(jamChannels)/sizeof(jamChannels[0]);
  int chIdx = 0;
  
  extern bool feature_exit_requested;  // from main
  while (!feature_exit_requested) {
    if (readButtonOnce(BTN_OK)) {
      active = !active;
      if (active) {
        tft.fillRect(10, 95, 220, 20, BG);
        tft.setTextColor(RED, BG);
        tft.setCursor(10, 95);
        tft.print(">>> JAMMING ACTIVE <<<");
        radio.startConstCarrier(RF24_PA_MAX, jamChannels[chIdx]);
      } else {
        radio.stopConstCarrier();
        tft.fillRect(10, 95, 220, 20, BG);
      }
      delay(200);
    }
    if (readButtonOnce(BTN_LEFT)) break;

    if (active) {
      radio.stopConstCarrier();
      chIdx = (chIdx + 1) % jamCount;
      radio.startConstCarrier(RF24_PA_MAX, jamChannels[chIdx]);
      delay(20);
    }
    delay(50);
  }
  radio.stopConstCarrier();
  radio.powerDown();
}

// ========== Attack: NRF24 Scanner ==========
void runNRF24Scanner() {
  tft.fillRect(0, 23, 240, 297, BG);
  drawStatusBarBright();
  tft.setTextColor(GREEN, BG);
  tft.setCursor(10, 35);
  tft.print("NRF24 Scanner");
  tft.setCursor(10, 55);
  tft.print("Scanning channels 0-125...");
  
  SPI.begin(NRF_SCK_PIN, NRF_MISO_PIN, NRF_MOSI_PIN);
  RF24 radio(NRF_CE_PIN, NRF_CSN_PIN);
  if (!radio.begin()) {
    tft.setTextColor(RED, BG);
    tft.setCursor(10, 75);
    tft.print("NRF24 not found!");
    delay(2000);
    return;
  }
  radio.setPALevel(RF24_PA_LOW);
  radio.startListening();
  
  int y = 75;
  tft.setTextColor(WHITE, BG);
  for (int ch = 0; ch <= 125; ch++) {
    radio.setChannel(ch);
    delay(5);
    if (radio.testCarrier()) {
      tft.setCursor(10, y);
      tft.printf("Noise on ch %d", ch);
      y += 20;
      if (y > 280) break;
    }
    if (readButtonOnce(BTN_LEFT)) break;
  }
  tft.setCursor(10, y+10);
  tft.print("Press OK to exit");
  while (!readButtonOnce(BTN_OK)) delay(50);
}

