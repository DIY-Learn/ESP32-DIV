    int y = 55 + i * 36;
    if (i == RF24_ATTACK_COUNT - 1) y += 5;
    uint16_t color = (i == rf24Selected) ? SELECT : GREEN;
    tft.setTextColor(color, BG);
    int iconX = 10;
    int iconY = y - 12;
    const uint16_t* iconPtr = nullptr;
